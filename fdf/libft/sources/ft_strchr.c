#include "../includes/libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*b;
	int		i;

	i = 0;
	b = (char *)s;
	while (b[i] && b[i] != (char)c)
		i++;
	if (b[i] == (char)c)
		return (&b[i]);
	return (NULL);
}
